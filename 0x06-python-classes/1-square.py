#!/usr/bin/python3
"""A class Square that defines a square"""


class Square:
    """Module of a square
    Attributes:
        __size (int): Describes the length of a square
    """
    def __init__(self, size):
        """Init the square attributes
        Args:
            size (int): the length of the square
        Returns:
            None
        """
        self.__size = size
